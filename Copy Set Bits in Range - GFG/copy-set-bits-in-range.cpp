// User function Template for C++
class Solution{
    public:
    int setSetBit(int x, int y, int l, int r){
        int temp = 1,i;
        for (i=0;i<l-1;i++) temp=temp<<1;
        
        while (i<r) {
            if (y & temp) x = x|temp;
            
            temp = temp<<1;
            i++;
        }
        
        return x;
    }
};

